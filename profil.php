        <p>
        <h8>Sesuai Undang-Undang Nomor 39 Tahun 2008 tentang Kementerian Negara, Kementerian Kominfo merupakan perangkat Pemerintah Republik Indonesia ini membidangi urusan yang ruang lingkupnya disebutkan dalam Undang-Undang Dasar Negara Republik Indonesia Tahun 1945, yaitu informasi dan komunikasi.

Kementerian Komunikasi dan Informatika mempunyai tugas menyelenggarakan urusan pemerintahan di bidang komunikasi dan informatika untuk membantu Presiden dalam menyelenggarakan pemerintahan negara. Kementerian Kominfo dipimpin oleh seorang Menteri Komunikasi dan Informatika (Menkominfo) yang sejak tanggal 23 Oktober 2019 dijabat oleh <b>Johnny Gerard Plate.</b><br><br>

<b>Bermula dari Deppen</b><br><br>

Kementerian Komunikasi dan Informatika, sebelumnya bernama "Departemen Penerangan" (1945-1999), "Kementerian Negara Komunikasi dan Informasi" (2001-2005), dan Departemen Komunikasi dan Informatika (Depkominfo) (2005-2009).<br><br>

Setelah proklamasi kemerdekaan dibentuk Lembaga Penerangan yang secara fungsional menjalankan kebijakan, pola dan pedoman penerangan dengan tujuan (1) membela dan mempertahankan kemerdekaan, (2) mengajak rakyat agar turut serta mempertahankan dan mengisi kemerdekaan serta (3) memperkenalkan Republik Indonesia di dan ke luar negeri.  Selama periode 1959-1965, sesuai Haluan Pembangunan Nasional sebagai ketetapan MPRS, Departemen Penerangan dibentuk untuk menyelenggarakan penerangan melalui media penerangan antara lain radio, film, toestel dan foto, percetakan, kendaraan, mesin stensil, dan mesin ketik.<br><br>

Mulai tahun 1966, salah satu tugas pokok organisasi penerangan adalah mengarahkan pendapat umum agar terbentuk dukungan, kontrol dan pratisipasi sosial yang positif terhadap pelaksanaan kebijaksanaan pemerintah, selain untuk penerangan ke dalam dan luar negeri. Pada masa itu pengorganisasian ke dalam dilakukan agar Departemen Penerangan berfungsi menjadi Juru Bicara Pemerintah. Tanggal 15 September 1967 wewenang penerangan luar negeri (Penlugri) yang sejak 1959 dipegang Departemen Luar Negeri RI dialihkan kembali pengelolaannya kepada Departemen Penerangan. <br><br>

Tahun 1971, untuk meningkatkan efisiensi dan efektivitas sarana media massa, dikembangkan sistem komunikasi terintegrasi melalui koordinasi, integrasi dan sinergi antarunsur-unsur penerangan pemerintah. Dibentuklah lembaga antara lain Badan Koordinasi Kehumasan Pemerintah (BAKOHUMAS) dan Badan Koordinasi Penerangan (BAKOPEN). <br><br> 

Di tingkat daerah, dibentuk jawatan penerangan provinsi, kantor penerangan kabupaten dan juru penerang di tingkat kecamatan. Di kabupaten dan kota dibentuk Pusat Penerangan Masyarakat (PUSPENMAS) dengan kegiatan utama penerangan antar pribadi didukung sarana penerangan di daerah seperti: radio, televisi, film penerangan, pers penerbitan, pameran dan pertunjukkan rakyat serta diskusi kerja. </h8>
