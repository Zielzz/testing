    <b>Masa Reformasi</b><br>
    Pada awal masa reformasi, tugas dan fungsi Departemen Penerangan tidak banyak berubah. Kelembagaan penerangan dipertahankan mulai dari tingkat pusat sampai provinsi dengan nama kantor wilayah departemen penerangan dan biro humas provinsi.  Pasca pemberlakuan Undang-undang Nomor 22 Tahun 1999 tentang Pemerintahan Daerah, terjadi  reposisi terhadap perangkat daerah sesuai kepentingan daerah. Kantor penerangan di bawah koordinasi pemerintah provinsi, kabupaten dan kota. <br><br>

Sesuai dengan Kepres 153 Tahun 1999 dibentuk Badan Informasi dan Komunikasi Nasional (BIKN) di tingkat pusat, sedang di tingkat provinsi; kabupaten dan kota. Setahun setelah pembubaran Departemen Penerangan, dibentuk Lembaga Informasi Nasional (LIN), yang kemudian diubah lagi statusnya menjadi Kementerian Negara Komunikasi dan Informasi (Kemeneg Kominfo). <br><br>

Selanjutnya, tugas layanan informasi publik diserahkan kepada Menteri Negara Komunikasi dan Informasi. Sedang BIKN berubah menjadi Lembaga Informasi Nasional dan bertanggung jawab kepada Menteri Komunikasi dan Informasi. <br><br>

Sejak tahun 2005, terjadi perubahan dari Kementerian Negara Komunikasi dan Informasi menjadi Departemen Komunikasi dan Informatika. Sesuai dengan Peraturan Presiden Nomor 9 Tahun 2005, dilakukan  integrasi Kementerian Negara Komunikasi dan Informasi;  Lembaga Informasi Nasional,  dan Direktorat Jenderal Pos dan Telekomunikasi. <br><br>

Salah satu fungsi penting Departemen Kominfo yang berkaitan dengan informasi adalah penyebarluasan informasi nasional dengan menciptakan keterbukaan akses informasi dan membangun serta mengembangkan infrastruktur telekomunikasi guna kepentingan seluruh warga negara. <br><br>

Akhir tahun 2010 lalu, lembaga Kementerian Komunikasi dan Informatika menyempurnakan penataan organisasi. Paradigma baru kebijakan komunikasi menempatkan informasi sebagai bagian kebutuhan keseharian masyarakat. Fungsi informasi dikembangkan pada nilai tambah ekonomi, bukan sekadar 'penerangan', namun lebih dukungan komunikasi strategis untuk membangun integrasi nasional dengan baik. <br><br>
